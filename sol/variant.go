func buildVariantPiles(v string) ([]*Pile, bool) {
	var piles []*Pile
	if vi, exists := Variants[v]; exists {
		for _, pi := range vi.Piles {
			p := NewPile(pi.Class, pi.X, pi.Y, pi.Fan, pi.Attributes)
			piles = append(piles, p)
		}
	}

	return piles, len(piles) > 0
}
